    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (tglAwal.getDate() != null || tglAkhir.getDate() != null) {
            Date mulai = tglAwal.getDate();
            Date selesai = tglAkhir.getDate();
            if (cmbFakultas.getSelectedItem() != -1) {
                if (cmbJurusan.getSelectedItem() != -1) {
                    if (cmbDosen.getSelectedItem() != -1) {
                        String fak = cmbFakultas.getSelectedItem().toString();
                        String jur = cmbJurusan.getSelectedItem().toString();
                        String dos = cmbDosen.getSelectedItem().toString();
                        displayResult(absendao.getCariAbsen(fak, jur, dos, mulai, selesai));
                    } else {
                        JOptionPane.showMessageDialog(this, "Pilihan Dosen Kosong", "Warning", JOptionPane.WARNING_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Pilihan Jurusan Kosong", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Pilihan Fakultas Kosong", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Tanggal Awal Atau Tanggal Akhir Kosong", "Warning", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

