    private void displayResult(List resultList) {
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("Fakultas");
        tableHeaders.add("Jurusan");
        tableHeaders.add("Dosen");
        tableHeaders.add("Mata Kuliah");
        tableHeaders.add("Kelas");
        tableHeaders.add("Ruangan");
        tableHeaders.add("Jam Mulai");
        tableHeaders.add("Jam Selesai");
        tableHeaders.add("Absen Masuk");
        tableHeaders.add("Absen Keluar");
        //tableHeaders.add("Operation");

        Iterator its = resultList.iterator();
        while (its.hasNext()) {
            TblAbsen data = (TblAbsen) its.next();
            Vector<Object> oneRow = new Vector<Object>();
//            oneRow.add(data.getIdAbsen());
//            oneRow.add(data.getAbsenMulai());
//            oneRow.add(data.getAbsenSelesai());
            oneRow.add(data.getTblJadwal().getTblJurusan().getTblFakultas().getFakultas());
            oneRow.add(data.getTblJadwal().getTblJurusan().getJurusan());
            oneRow.add(data.getTblJadwal().getTblDosen().getNamaDosen());
            oneRow.add(data.getTblJadwal().getTblMakul().getMataKuliah());
            oneRow.add(data.getTblJadwal().getTblKelas().getKelas());
            oneRow.add(data.getTblJadwal().getTblRuangan().getNamaRuangan());
            oneRow.add(data.getTblJadwal().getJamMulai());
            oneRow.add(data.getTblJadwal().getJamSelesai());
            oneRow.add(data.getAbsenMulai());
            oneRow.add(data.getAbsenSelesai());
            //System.out.println(str[0] + str[1] + str[2]);
            tableData.add(oneRow);
        }
        resultTable.setModel(new DefaultTableModel(tableData, tableHeaders) {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
    }

    public void initFakultas() {
        cmbFakultas.removeAllItems();
        Iterator it = fakdao.getAllFakultas().iterator();
        while (it.hasNext()) {
            TblFakultas data = (TblFakultas) it.next();
            cmbFakultas.addItem(data.getFakultas());
        }
    }

    public void initJurusan(String fak) {
        cmbJurusan.removeAllItems();
        Iterator it = jurdao.getJurusanByFak(fak).iterator();
        while (it.hasNext()) {
            TblJurusan data = (TblJurusan) it.next();
            cmbJurusan.addItem(data.getJurusan());
        }
    }

    public void initDosen(String jur) {
        cmbDosen.removeAllItems();
        Iterator it = dosendao.getDosenByJur(jur).iterator();
        while (it.hasNext()) {
            TblDosen data = (TblDosen) it.next();
            cmbDosen.addItem(data.getNamaDosen());
        }
    }

    public void Refresh() {
        displayResult(absendao.getAllAbsen());
    }

